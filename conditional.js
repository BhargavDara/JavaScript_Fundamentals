//! if condition
let mode="on"
if(mode=="on")
{
  console.log("light is on")
  mode="off"
}

//! if else condition
if(mode=="on")
{
  console.log("light is on")
}
else{
  console.log("light is off")
}

//! conditional statement using ternary operator
mode=="on"?console.log("light is on"):console.log("light is off")